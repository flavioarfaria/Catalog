package com.flaviofaria.catalog.gradle.codegen.writer

import com.flaviofaria.catalog.gradle.codegen.ResourceEntry
import com.google.common.truth.Truth.assertThat
import java.io.File
import java.nio.charset.Charset
import org.junit.Before
import org.junit.Rule
import org.junit.Test
import org.junit.rules.TemporaryFolder

class ColorCatalogWriterTest {

  @get:Rule
  var folder = TemporaryFolder()

  private lateinit var codegenDestination: File

  private val resources = listOf(
    ResourceEntry.XmlItem.Color(
      file = File("."),
      name = "color_1",
      docs = "Color 1 docs",
    ),
    ResourceEntry.XmlItem.Color(
      file = File("."),
      name = "color_2",
      docs = null,
    ),
  )

  private lateinit var codegenFile: File
  private val writer = ColorCatalogWriter(packageName = "com.example")

  @Before
  fun setUp() {
    codegenDestination = folder.newFolder()
    codegenFile = File("${codegenDestination.absolutePath}/com/example/Colors.kt")
  }

  @Test
  fun `GIVEN generateResourcesExtensions and generateComposeExtensions disabled THEN generate property extensions only`() {
    writer.write(
      resources,
      sourceSetName = "main",
      codegenDestination = codegenDestination,
      generateResourcesExtensions = false,
      generateComposeExtensions = false,
    )
    assertThat(
      codegenFile.readBytes().toString(Charset.defaultCharset()),
    ).isEqualTo(
      """
      |// Auto-generated by Catalog. DO NOT EDIT.
      |// https://github.com/flavioarfaria/Catalog
      |@file:JvmName("ColorsMain")
      |@file:Suppress("NOTHING_TO_INLINE")
      |
      |package com.example
      |
      |import androidx.`annotation`.ColorRes
      |import com.flaviofaria.catalog.runtime.resources.Colors
      |import kotlin.Int
      |import kotlin.Suppress
      |import kotlin.jvm.JvmName
      |
      |/**
      | * Color 1 docs
      | */
      |@get:ColorRes
      |public inline val Colors.color1: Int
      |  get() = R.color.color_1
      |
      |@get:ColorRes
      |public inline val Colors.color2: Int
      |  get() = R.color.color_2
      |""".trimMargin(),
    )
  }

  @Test
  fun `GIVEN generateResourcesExtensions enabled and generateComposeExtensions disabled THEN generate property and resources extensions only`() {
    writer.write(
      resources,
      sourceSetName = "main",
      codegenDestination = codegenDestination,
      generateResourcesExtensions = true,
      generateComposeExtensions = false,
    )
    assertThat(
      codegenFile.readBytes().toString(Charset.defaultCharset()),
    ).isEqualTo(
      """
      |// Auto-generated by Catalog. DO NOT EDIT.
      |// https://github.com/flavioarfaria/Catalog
      |@file:JvmName("ColorsMain")
      |@file:Suppress("NOTHING_TO_INLINE")
      |
      |package com.example
      |
      |import android.content.Context
      |import androidx.`annotation`.ColorInt
      |import androidx.`annotation`.ColorRes
      |import androidx.core.content.ContextCompat
      |import androidx.fragment.app.Fragment
      |import com.flaviofaria.catalog.runtime.resources.Colors
      |import kotlin.Int
      |import kotlin.Suppress
      |import kotlin.jvm.JvmName
      |
      |/**
      | * Color 1 docs
      | */
      |@get:ColorRes
      |public inline val Colors.color1: Int
      |  get() = R.color.color_1
      |
      |/**
      | * Color 1 docs
      | */
      |context(Context)
      |@ColorInt
      |public inline fun Colors.color1(): Int = ContextCompat.getColor(this@Context, R.color.color_1)
      |
      |/**
      | * Color 1 docs
      | */
      |context(Fragment)
      |@ColorInt
      |public inline fun Colors.color1(): Int = ContextCompat.getColor(requireContext(), R.color.color_1)
      |
      |@get:ColorRes
      |public inline val Colors.color2: Int
      |  get() = R.color.color_2
      |
      |context(Context)
      |@ColorInt
      |public inline fun Colors.color2(): Int = ContextCompat.getColor(this@Context, R.color.color_2)
      |
      |context(Fragment)
      |@ColorInt
      |public inline fun Colors.color2(): Int = ContextCompat.getColor(requireContext(), R.color.color_2)
      |""".trimMargin(),
    )
  }

  @Test
  fun `GIVEN generateResourcesExtensions disabled and generateComposeExtensions enabled THEN generate property and compose extensions only`() {
    writer.write(
      resources,
      sourceSetName = "main",
      codegenDestination = codegenDestination,
      generateResourcesExtensions = false,
      generateComposeExtensions = true,
    )
    assertThat(
      codegenFile.readBytes().toString(Charset.defaultCharset()),
    ).isEqualTo(
      """
      |// Auto-generated by Catalog. DO NOT EDIT.
      |// https://github.com/flavioarfaria/Catalog
      |@file:JvmName("ColorsMain")
      |@file:Suppress("NOTHING_TO_INLINE")
      |
      |package com.example
      |
      |import androidx.`annotation`.ColorRes
      |import androidx.compose.runtime.Composable
      |import androidx.compose.runtime.ReadOnlyComposable
      |import androidx.compose.ui.graphics.Color
      |import androidx.compose.ui.res.colorResource
      |import com.flaviofaria.catalog.runtime.compose.Colors
      |import kotlin.Int
      |import kotlin.Suppress
      |import kotlin.jvm.JvmName
      |
      |/**
      | * Color 1 docs
      | */
      |@get:ColorRes
      |public inline val Colors.color1: Int
      |  get() = R.color.color_1
      |
      |/**
      | * Color 1 docs
      | */
      |@Composable
      |@ReadOnlyComposable
      |public inline fun Colors.color1(): Color = colorResource(R.color.color_1)
      |
      |@get:ColorRes
      |public inline val Colors.color2: Int
      |  get() = R.color.color_2
      |
      |@Composable
      |@ReadOnlyComposable
      |public inline fun Colors.color2(): Color = colorResource(R.color.color_2)
      |""".trimMargin(),
    )
  }

  @Test
  fun `GIVEN generateResources and generateComposeExtensions enabled THEN generate property, resources and compose extensions`() {
    writer.write(
      resources,
      sourceSetName = "main",
      codegenDestination = codegenDestination,
      generateResourcesExtensions = true,
      generateComposeExtensions = true,
    )
    assertThat(
      codegenFile.readBytes().toString(Charset.defaultCharset()),
    ).isEqualTo(
      """
      |// Auto-generated by Catalog. DO NOT EDIT.
      |// https://github.com/flavioarfaria/Catalog
      |@file:JvmName("ColorsMain")
      |@file:Suppress("NOTHING_TO_INLINE")
      |
      |package com.example
      |
      |import android.content.Context
      |import androidx.`annotation`.ColorInt
      |import androidx.`annotation`.ColorRes
      |import androidx.compose.runtime.Composable
      |import androidx.compose.runtime.ReadOnlyComposable
      |import androidx.compose.ui.graphics.Color
      |import androidx.compose.ui.res.colorResource
      |import androidx.core.content.ContextCompat
      |import androidx.fragment.app.Fragment
      |import com.flaviofaria.catalog.runtime.compose.Colors
      |import kotlin.Int
      |import kotlin.Suppress
      |import kotlin.jvm.JvmName
      |
      |/**
      | * Color 1 docs
      | */
      |@get:ColorRes
      |public inline val Colors.color1: Int
      |  get() = R.color.color_1
      |
      |/**
      | * Color 1 docs
      | */
      |context(Context)
      |@ColorInt
      |public inline fun com.flaviofaria.catalog.runtime.resources.Colors.color1(): Int =
      |    ContextCompat.getColor(this@Context, R.color.color_1)
      |
      |/**
      | * Color 1 docs
      | */
      |context(Fragment)
      |@ColorInt
      |public inline fun com.flaviofaria.catalog.runtime.resources.Colors.color1(): Int =
      |    ContextCompat.getColor(requireContext(), R.color.color_1)
      |
      |/**
      | * Color 1 docs
      | */
      |@Composable
      |@ReadOnlyComposable
      |public inline fun Colors.color1(): Color = colorResource(R.color.color_1)
      |
      |@get:ColorRes
      |public inline val Colors.color2: Int
      |  get() = R.color.color_2
      |
      |context(Context)
      |@ColorInt
      |public inline fun com.flaviofaria.catalog.runtime.resources.Colors.color2(): Int =
      |    ContextCompat.getColor(this@Context, R.color.color_2)
      |
      |context(Fragment)
      |@ColorInt
      |public inline fun com.flaviofaria.catalog.runtime.resources.Colors.color2(): Int =
      |    ContextCompat.getColor(requireContext(), R.color.color_2)
      |
      |@Composable
      |@ReadOnlyComposable
      |public inline fun Colors.color2(): Color = colorResource(R.color.color_2)
      |""".trimMargin(),
    )
  }
}
